package org.kaazing.gateway.server.spi.security;

import javax.security.auth.callback.Callback;

/**
 * A token callback class retrieves the authentication token presented to
 * the gateway for use in a {@link javax.security.auth.spi.LoginModule}.
 */
public class AuthenticationTokenCallback implements Callback {

   AuthenticationToken authenticationToken;

    public AuthenticationToken getAuthenticationToken() {
        return authenticationToken;
    }

    public void setAuthenticationToken(AuthenticationToken authenticationToken) {
        this.authenticationToken = authenticationToken;
    }

}
